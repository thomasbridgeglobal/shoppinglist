<template>
  <div id="app" class="container">
    <h2>{{ title }}</h2>

  <div class="input-group">
    <input v-model="newItem" v-on:keyup.enter="addItem" placeholder="add shopping list item" type="text" class="form-control">
    <span class="input-group-btn">
    <button v-on:click="addItem" class="btn btn-default" type="button">Add!</button>
    </span>
  </div>

    <ul class="list-group"> 
      <li class="list-group-item" v-for="item in items" v-bind:class="{ 'removed': item.checked }" :key="item.text" >
        <div class="checkbox">
        <label>
        <input type="checkbox" v-model="item.checked"> {{ item.text }}
        </label>
        </div>
      </li>      
    </ul>
    <div class="footer">
    <hr/>
    <em>Change the title of your shopping list here</em>
    <input v-model="title"/>
    </div>
    <hello-world  msg="hello there"></hello-world>

  </div>
</template>

<script>
 import HelloWorld from './components/HelloWorld.vue'

export default {
  name: 'App',
  components: {
    HelloWorld
  },
  data: function(){
    return {      
      title: "Shopping list",
      items: [{text: "Toy guns", checked: false }, {text: "criket bat", checked: false }],
      newItem: ""
    }
  },
  methods: {
  addItem: function () {
    var text;
    text = this.newItem.trim();
    if (text) {
      this.items.push({
        text: text,
        checked: false
      });
      this.newItem = '';
    }
  }
}
}
</script>

